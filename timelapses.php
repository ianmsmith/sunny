<h3>Brief description</h3>
<p>As the sun moves across the sky near the beginning of autumn, the shadow of the gnomon (projecting tube and ball) moves across the southwest-facing wall and indicates the time to an accuracy of one minute.
</p>
<p>
Time coverage: 20 Sept 2011 0600-2000 PDT (0500-1900 PST, 05:03-19:03 local solar time)<br>
Exact time of autumnal equinox: 2.5 days later at 0204 PDT on 23 Sept<br>
Sunrise:    0653 PDT<br>
Solar noon: 1303 PDT<br>
Sunset:     1912 PDT<br>
Sunlight falls on the wall: 1000-1912 PDT; elevation of sun varies 30&deg; to 0&deg;<br>
<br>
Images captured every 1.0 minutes; movie shown at 24 frames/sec; time label is PDT.
</p>
<h3>Commentary (also see comments for summer solstice movie)</h3>
<p>
The sundial wall is seen over a 14-hour period (compressed to 35 seconds), from before sunrise until after sunset. The wall faces 36&deg; to the west of due south, and so the sun does not shine on it until 1030 PDT, many hours after sunrise. The track of the ball's shadow would exactly follow the equinox line if the observations were indeed on the equinox, but in fact they are ~2.5 days early. Thus the solar declination is +1.0&deg; (and decreasing at the very fast rate of 0.4&deg;/day), and the ball's shadow tracks <i>slightly below</i> the equinox line. In fact it is a slightly-curved hyperbola and one can see in the animation that it is closest to the equinox line in mid-afternoon. The equinox shadow terminates on the sunset line when the local solar time is exactly 6 hours past noon.
</p>
