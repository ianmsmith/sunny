<h3>Brief description</h3>
<p>As the sun moves high across the sky on the longest day of the year, the shadow of the gnomon (projecting tube and ball) moves across the southwest-facing wall and indicates the time to an accuracy of one minute.
</p>
<p>
Time coverage: 21 June 2011 0500-2200 PDT (0400-2100 PST, 03:49-20:49 local solar time)<br>
Exact time of summer solstice: 1016 PDT<br>
Sunrise:    0511 PDT<br>
Solar noon: 1311 PDT<br>
Sunset:     2111 PDT<br>
Sunlight falls on the wall:  1116-2107 PDT; elevation of sun varies 57&deg; to 0&deg;<br>
<br>
Images captured every 1.0 minutes; movie shown at 24 frames/sec; time label is PDT<br>
</p>
<h3>Commentary</h3>
<p>
The sundial wall is seen over a 17-hour period (compressed to 52 seconds), from well before sunrise until after sunset. The wall faces 36&deg; to the west of due south, and so the sun does not shine on it until 1130 PDT, over six hours after sunrise. Furthermore, we are not viewing the wall perpendicularly, but rather from ~27&deg; to the east side of perpendicular <a href="setup.php">(see diagram of our setup)</a>. The entire day was very clear, although a few small shadowless gaps in the afternoon were caused by intermittent clouds. The southwest orientation of the wall leads to the dial's asymmetrical design and its mainly working in the afternoon. It also means that the spacing of the hour lines, and thus the speed of the shadow, is very different in the morning and afternoon. Also note how the elongation of the ball's shadow dramatically changes over the day.</p>
<p>
Because the various "lines" comprising the hour and date lines of the sundial are in fact bars and rods held off the wall by 2-3 inches (5-7 cm), they each cast their own shadow on the brick wall, which makes for interesting effects in the movie, especially when the sunlight is very oblique to the wall. The most striking effect of this sort can be seen at ~1130 PDT when the sun (already high in the sky) first comes on to the wall and shadows are very elongated and displaced -- note especially that of the winter solstice line (the line along the top).</p>

